    private void inicializarValores() {
        // Obtener datos del pedido
        PedidoDTO pedido = subsistemaSAT.obtenerPedidoPorId(pedidoId);
        if (pedido != null) {
            jLabelPedidoId.setText(String.valueOf(pedido.getPedidoid()));
            int precioTotal = pedido.getPrecioTotal();
            jLabelPrecioTotal.setText(String.valueOf(precioTotal));
            // Obtener datos del cliente
            ClienteDTO cliente = subsistemaSAT.obtenerClientePorId(pedido.getClienteId());
            System.out.println(cliente.toString());
            if (cliente != null) {
                jLabelNombre.setText(cliente.getNombre());
                jLabelApellido.setText(cliente.getApellido());
                jLabelCorreo.setText(cliente.getCorreo());
            }

            // Obtener datos de la factura
            FacturaDTO factura = subsistemaSAT.obtenerFacturaPorPedidoId(pedidoId);
            if (factura != null) {
                jLabelRFC.setText(factura.getRFC());
                jLabelUsoFiscal.setText(factura.getUsoFiscal());
                jLabelCURP.setText(factura.getCURP());
            }
            jLabelFechaActual.setText(fechaActual);
            LocalDate fechaActual = LocalDate.now();
            LocalDate fechaVencimiento = fechaActual.plusMonths(1);
            String fechaVencimientoStr = fechaVencimiento.toString();
            FechaVencimiento.setText(fechaVencimientoStr);
            Random random = new Random();
            int numeroFactura = random.nextInt(1000) + 1; // Random number between 1 and 1000
            numeroFac.setText(Integer.toString(numeroFactura));
        }
    }

